using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Data;
using System.Drawing;
using System.Linq;
using System.Text;
using System.Threading;
using System.Threading.Tasks;
using System.Windows.Forms;

namespace App_JC4._0
{
   

    public partial class coundown : Form
    {
        main f = new main();

        public static int counter = 10;
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        public coundown()
        {
            InitializeComponent();
        }
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        private void coundown_Load(object sender, EventArgs e)
        {
            timer3.Start();
        }
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        private void button1_Click(object sender, EventArgs e)
        {
            timer3.Stop();

            

            this.Hide();

            f.ShowDialog();

        }
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        private void coundown_FormClosing(object sender, FormClosingEventArgs e)
        {
            timer3.Stop();



            this.Hide();
            f.ShowDialog();
        }
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        private void timer3_Tick(object sender, EventArgs e)
        {
            label1.Text = counter.ToString();
            progressBar1.Value = 10-counter;

            counter--;
            if (counter == 0)
            {                                
                label1.Text = counter.ToString();
                progressBar1.Value = 10-counter;
                Application.DoEvents();

                timer3.Stop();

                this.Hide();
                f.ShowDialog();
            }


            label1.Text = counter.ToString();
            progressBar1.Value = 10-counter;
        }
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
        //----------------------------------------------------------------------------------------------------------------------------------------------------------------
    }
}
